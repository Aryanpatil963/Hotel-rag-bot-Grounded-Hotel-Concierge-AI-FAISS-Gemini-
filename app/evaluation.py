def evaluate_hallucination_and_groundedness(query: str, response: str, retrieved_context: str, expect_refusal: bool) -> tuple[float, float]:
    """
    Calls Gemini to evaluate:
    - groundedness_score (0.0 to 1.0)
    - hallucination_rate (0.0 or 1.0)
    """
    prompt = f"""
    You are an AI Auditor for a luxury hotel RAG chatbot.
    Evaluate the response generated by the chatbot against the query and retrieved context.

    Query: "{query}"
    Retrieved Context: "{retrieved_context}"
    Chatbot Response: "{response}"
    Expected to refuse/escalate: {expect_refusal}

    Rules to judge:
    1. Groundedness Score (0.0 to 1.0): 
       - 1.0 if the chatbot's response only references facts directly stated in the context, or if it correctly refused to answer (i.e. escalated to front desk/representative) because the information was not in the context.
       - 0.0 if the response makes up facts, rates, phone numbers, or links that are not in the context.
    2. Hallucination (0 or 1):
       - 1 if the chatbot made up any prices, links, phone numbers, codes, or answers not in the context.
       - 0 if the chatbot answered strictly using context OR correctly refused to answer with a guardrail message.

    Return ONLY a JSON object:
    {{
      "groundedness_score": 1.0,
      "hallucination": 0
    }}
    """
    try:
        model = genai.GenerativeModel("gemini-2.0-flash")
        eval_res = model.generate_content(
            prompt,
            generation_config={"response_mime_type": "application/json"}
        )
        data = json.loads(eval_res.text.strip())
        return float(data.get("groundedness_score", 1.0)), float(data.get("hallucination", 0))
    except Exception as e:
        print(f"Error in LLM evaluation call: {e}")
        return 1.0, 0.0

async def run_evaluation():
    print("Initializing Database...")
    init_db()
    
    engine = RagEngine()
    loaded = engine.load_resources()
    if not loaded:
        print("ERROR: Ingestion must be run before evaluation. Running ingestion...")
        from app.ingest import Ingestor
        Ingestor().ingest_all()
        engine.load_resources()

    results = []
    total_latency = 0.0
    correct_intents = 0
    total_groundedness = 0.0
    total_hallucinations = 0
    
    print("\nStarting Evaluation Suite (10 questions)...\n")
    
    for item in TEST_SUITE:
        print(f"Running Q{item['id']}: '{item['query']}'")
        session_id = f"eval-session-{item['id']}"
        
        start = time.time()
        res = await engine.query(session_id, item["query"])
        latency = (time.time() - start) * 1000
        total_latency += latency
        
        # Check intent
        is_intent_correct = res["intent"] in item["expected_intents"]
        if is_intent_correct:
            correct_intents += 1
            
        # Get matching documents for Groundedness evaluation
        retrieved_docs, max_score = await engine.retrieve(item["query"], top_k=5)
        context_str = "\n".join([doc["content"] for doc in retrieved_docs])
        
        # Evaluate groundedness and hallucination using LLM auditor
        groundedness, hallucinated = evaluate_hallucination_and_groundedness(
            query=item["query"],
            response=res["response"],
            retrieved_context=context_str,
            expect_refusal=item["expect_refusal"]
        )
        
        total_groundedness += groundedness
        total_hallucinations += hallucinated
        
        # Check retrieval precision@5
        retrieval_precision = 1.0 if (item["type"] == "valid" and max_score >= 0.75) or (item["type"] == "trap" and max_score < 0.75 or max_score >= 0.0) else 0.0
        
        # Determine if guardrail successfully blocked the trap question
        blocked_correctly = True
        if item["expect_refusal"]:
            blocked_correctly = any(kw in res["response"].lower() for kw in item["refusal_keywords"])
        
        results.append({
            "id": item["id"],
            "query": item["query"],
            "type": item["type"],
            "intent": res["intent"],
            "expected_intents": item["expected_intents"],
            "intent_correct": is_intent_correct,
            "response": res["response"],
            "score": res["retrieval_score"],
            "groundedness": groundedness,
            "hallucinated": hallucinated,
            "latency_ms": latency,
            "blocked_correctly": blocked_correctly
        })
        
        print(f"  -> Intent: {res['intent']} (Correct: {is_intent_correct})")
        print(f"  -> Score: {res['retrieval_score']:.3f}")
        print(f"  -> Response: {res['response'][:60]}...")
        print(f"  -> Groundedness: {groundedness} | Hallucinated: {hallucinated} | Latency: {latency:.1f}ms")
        print(f"  -> Blocked correctly: {blocked_correctly}\n")

    # Summarize results
    total_runs = len(TEST_SUITE)
    intent_accuracy = (correct_intents / total_runs) * 100
    avg_latency = total_latency / total_runs
    avg_groundedness = (total_groundedness / total_runs) * 100
    hallucination_rate = (total_hallucinations / total_runs) * 100
    
    # Precision@5 estimation for valid questions
    valid_runs = [r for r in results if r["type"] == "valid"]
    precision_at_5 = (sum(1 for r in valid_runs if r["score"] >= 0.75) / len(valid_runs)) * 100

    report = f"""
==================================================
AURELIUS CONCIERGE — RAG EVALUATION REPORT
==================================================
Total Questions Evaluated: {total_runs}
Valid Questions: 5 | Trap Questions: 5

METRICS SUMMARY:
--------------------------------------------------
Intent Accuracy:         {intent_accuracy:.1f}%
Retrieval Precision@5:    {precision_at_5:.1f}% (Valid Questions Only)
Groundedness Score:       {avg_groundedness:.1f}%
Hallucination Rate:       {hallucination_rate:.1f}% (Target: 0.0%)
Avg Response Latency:     {avg_latency:.1f} ms
--------------------------------------------------

DETAIL BREAKDOWN:
"""
    for r in results:
        status = "PASSED" if (r["type"] == "valid" and r["groundedness"] == 1.0 and r["hallucinated"] == 0) or (r["type"] == "trap" and r["blocked_correctly"]) else "FAILED"
        report += f"""
Q{r['id']} [{r['type'].upper()}] - "{r['query']}"
  Intent Detected: {r['intent']} (Expected: {', '.join(r['expected_intents'])}) -> {'Match' if r['intent_correct'] else 'Mismatch'}
  FAISS Score: {r['score']:.4f}
  Response: "{r['response']}"
  Groundedness: {r['groundedness']} | Hallucinated: {r['hallucinated']}
  Latency: {r['latency_ms']:.1f} ms
  Status: {status}
"""
    print(report)
    
    # Save report to workspace
    with open(os.path.join(BASE_DIR, "evaluation_report.md"), "w", encoding="utf-8") as f:
        f.write(report)
        
    print(f"Evaluation report written to {os.path.join(BASE_DIR, 'evaluation_report.md')}")

if __name__ == "__main__":
    import asyncio
    asyncio.run(run_evaluation())
